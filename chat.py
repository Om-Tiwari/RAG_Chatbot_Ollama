from openai import OpenAI
import streamlit as st
import ollama

st.set_page_config(page_title="Ollama", page_icon=":robot:", layout="wide", initial_sidebar_state="expanded")

def extract_model_names(model_info):
    return tuple(model["name"] for model in model_info["models"])

def main():
    """
    The main function that runs the application.
    """

    client = OpenAI(
        base_url="http://localhost:1000/v1",
        api_key="ollama",  # required, but unused
    )

    models_info = ollama.list()
    available_models = extract_model_names(models_info)

    if available_models:
        selected_model = st.selectbox(
            "Pick a model available locally on your system â†“", available_models
        )

    else:
        st.warning("You have not pulled any model from Ollama yet!", icon="âš ï¸")
        if st.button("Go to settings to download a model"):
            st.page_switch("pages/03_âš™ï¸_Settings.py")

    message_container = st.container(height=500, border=True)

    if "messages" not in st.session_state:
        st.session_state.messages = []

    for message in st.session_state.messages:
        avatar = "ğŸ¤–" if message["role"] == "assistant" else "ğŸ˜"
        with message_container.chat_message(message["role"], avatar=avatar):
            st.markdown(message["content"])

    if prompt := st.chat_input("Enter a prompt here..."):
        try:
            st.session_state.messages.append(
                {"role": "user", "content": prompt})

            message_container.chat_message("user", avatar="ğŸ˜").markdown(prompt)

            with message_container.chat_message("assistant", avatar="ğŸ¤–"):
                with st.spinner("model working..."):
                    stream = client.chat.completions.create(
                        model=selected_model,
                        messages=[
                            {"role": m["role"], "content": m["content"]}
                            for m in st.session_state.messages
                        ],
                        stream=True,
                    )
                # stream response
                response = st.write_stream(stream)
            st.session_state.messages.append(
                {"role": "assistant", "content": response})

        except Exception as e:
            st.error(e, icon="â›”ï¸")


if __name__ == "__main__":
    main()